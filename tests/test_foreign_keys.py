class FKAuthor(BaseModel):
    __tablename__ = "fk_authors"
    id   = IntField(primary_key=True)
    name = StrField(max_length=100, nullable=False)


class FKBook(BaseModel):
    __tablename__ = "fk_books"
    id        = IntField(primary_key=True)
    title     = StrField(max_length=200, nullable=False)
    author_id = ForeignKeyField(to="FKAuthor", nullable=False)


class FKCompositeTarget(BaseModel):
    __tablename__ = "fk_composite_target"
    __pk__        = ("a", "b")
    a = IntField(nullable=False)
    b = IntField(nullable=False)


class FKRefToComposite(BaseModel):
    __tablename__ = "fk_ref_to_composite"
    id        = IntField(primary_key=True)
    target_id = ForeignKeyField(to="FKCompositeTarget", nullable=False)


class FKRefToMissing(BaseModel):
    __tablename__ = "fk_ref_to_missing"
    id      = IntField(primary_key=True)
    ghost_id = ForeignKeyField(to="NoSuchModelXYZ", nullable=False)


class FKPublisher(BaseModel):
    __tablename__ = "fk_publishers"
    id   = IntField(primary_key=True)
    name = StrField(max_length=100, nullable=False)


class FKBookCascade(BaseModel):
    __tablename__ = "fk_books_cascade"
    id           = IntField(primary_key=True)
    title        = StrField(max_length=200, nullable=False)
    publisher_id = ForeignKeyField(to="FKPublisher", nullable=False, on_delete="CASCADE")


class FKBookSetNull(BaseModel):
    __tablename__ = "fk_books_set_null"
    id           = IntField(primary_key=True)
    title        = StrField(max_length=200, nullable=False)
    publisher_id = ForeignKeyField(to="FKPublisher", nullable=True, on_delete="SET NULL")


